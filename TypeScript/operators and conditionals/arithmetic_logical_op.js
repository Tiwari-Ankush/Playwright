"use strict";
let totalItems = 10;
let itemsRemoved = 2;
console.log(totalItems + itemsRemoved); // 12 (Addition)
console.log(totalItems - itemsRemoved); // 8  (Subtraction)
console.log(totalItems * 2); // 20 (Multiplication)
console.log(totalItems / 2); // 5  (Division)
console.log(totalItems % 3); // 1  (Modulus/Remainder)
